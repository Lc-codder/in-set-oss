-- Exercício 2: Sistema de Gerenciamento de Plataforma de Streaming de Vídeos

create database streaming;
use streaming

CREATE TABLE plano (
	id INT AUTO_INCREMENT PRIMARY KEY,
    nome_plano varchar(20) NOT NULL CHECK (nome_plano IN ('plano basico', 'plano gold', 'livre acesso')),
    preco_mensal decimal(5,2) NOT NULL, 
    resolucao_maxima varchar(10) NOT NULL CHECK (resolucao_maxima IN ('720p', '1080p', '4K')),
    numero_telas smallint NOT NULL CHECK (numero_telas BETWEEN 1 AND 4), 
    super_planos varchar(50) CHECK (super_planos IN ('netflix + prime', 'prime + disney plus', 'disney plus + netflix', 'netflix + prime + disney plus'))
);


CREATE TABLE conteudo (
	id INT AUTO_INCREMENT PRIMARY KEY,
    titulo varchar(50) NOT NULL,
    tipo varchar(50) NOT NULL CHECK (tipo IN ('filme', 'série', 'documentário', 'animação')),
    genero varchar(20) NOT NULL,
    ano_lancamento integer NOT NULL,  
    duracao_minutos integer NOT NULL,  
    classificacao varchar(5) CHECK (classificacao IN ('L', '10', '12', '14', '16', '18')),
    status_disponibilidade varchar(20) NOT NULL CHECK (status_disponibilidade IN ('incluido no plano basico', 'somente no plano gold', 'livre acesso')),
    idiomas_disponiveis varchar(20) NOT NULL CHECK (idiomas_disponiveis IN ('portugues', 'ingles', 'espanhol'))
);


CREATE TABLE usuario (
    id INT AUTO_INCREMENT PRIMARY KEY,
    nome varchar(50) NOT NULL, 
    apelido varchar(30) NOT NULL,
    email varchar(50) UNIQUE NOT NULL,
    telefone varchar(20) NOT NULL,
    data_cadastro date NOT NULL,  
    plano_id INT NOT NULL,
    FOREIGN KEY (plano_id) REFERENCES plano(id)
);


CREATE TABLE historico (
    id INT AUTO_INCREMENT PRIMARY KEY,
    usuario_id integer NOT NULL,
    conteudo_id integer NOT NULL, 
    data_visualizacao timestamp NOT NULL DEFAULT CURRENT_TIMESTAMP,  
    progresso varchar(4) NOT NULL,  
    favoritos boolean DEFAULT FALSE,  
    FOREIGN KEY (usuario_id) REFERENCES usuario(id),
    FOREIGN KEY (conteudo_id) REFERENCES conteudo(id)
);


CREATE TABLE avaliacao (
    id INT AUTO_INCREMENT PRIMARY KEY,
    usuario_id integer NOT NULL,
    conteudo_id integer NOT NULL,
    nota smallint NOT NULL CHECK (nota BETWEEN 0 AND 5),  
    comentarios varchar(200),  
    data_avaliacao date NOT NULL,
    FOREIGN KEY (usuario_id) REFERENCES usuario(id),
    FOREIGN KEY (conteudo_id) REFERENCES conteudo(id),
    UNIQUE (usuario_id, conteudo_id)  
);

